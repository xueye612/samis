const applyScheduleDefaults = (item: SurgeryCase): SurgeryCase => {
  const override = scheduleOverrides[item.id] ?? {};
  const merged = { ...item, ...override };
  const scheduledStart = merged.scheduledStart ?? merged.plannedStart;
  const scheduledEnd =
    merged.scheduledEnd ??
    merged.surgeryEnd ??
    merged.leaveRoomTime ??
    dayjs(scheduledStart).add(merged.expectedDurationMinutes || 60, 'minute').toISOString();

  return {
    ...merged,
    patientId: merged.patientId ?? `patient-${merged.id.replace(/^case-/, '')}`,
    roomId: merged.roomId ?? merged.room,
    roomName: merged.roomName ?? merged.room,
    scheduledStart,
    scheduledEnd,
    actualStart: merged.actualStart ?? merged.anesthesiaStart,
    assignedAnesthesiologistIds: merged.assignedAnesthesiologistIds ?? [merged.anesthesiologist],
    assignedNurseIds: merged.assignedNurseIds ?? [merged.anesthesiaNurse],
    nursingScheduleSource: merged.nursingScheduleSource ?? '手术护理系统正式排班',
    emergencyInserted: merged.emergencyInserted ?? false,
    recordStatus: merged.recordStatus ?? (merged.locked ? '已锁定' : merged.anesthesiaStart ? '采集中' : '未开始'),
    collectStatus: merged.collectStatus ?? (merged.anesthesiaStart ? '采集中' : '未连接'),
    vitalFrequency: merged.vitalFrequency ?? '5分钟',
    anesthesiaPlanes: merged.anesthesiaPlanes ?? (merged.anesthesiaMethod.includes('椎管') || merged.anesthesiaMethod.includes('腰硬') ? [
      { id: `${merged.id}-plane-1`, time: merged.anesthesiaStart ?? scheduledStart, level: 'T6', direction: 'down', remark: '阻滞平面' },
    ] : []),
    operationLogs: merged.operationLogs ?? ['手术护理系统同步排班', '麻醉侧接收患者基础信息'],
  };
};

